namespace Stackdose.UI.Core.Models
{
    /// <summary>
    /// 按鈕顏色主題 (Button Color Theme)
    /// </summary>
    public enum ButtonTheme
    {
        /// <summary>
        /// 預設主題 (灰藍色)
        /// </summary>
        Normal,

        /// <summary>
        /// 主要主題 (藍色)
        /// </summary>
        Primary,

        /// <summary>
        /// 成功主題 (綠色)
        /// </summary>
        Success,

        /// <summary>
        /// 警告主題 (橙色)
        /// </summary>
        Warning,

        /// <summary>
        /// 錯誤主題 (紅色)
        /// </summary>
        Error,

        /// <summary>
        /// 資訊主題 (青色)
        /// </summary>
        Info
    }
}
